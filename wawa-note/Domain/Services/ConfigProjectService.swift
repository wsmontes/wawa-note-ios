import Foundation
import SwiftData

/// Manages the special wawa-note-config project that contains all app configuration as editable files.
/// This project is auto-created on first launch and hidden from regular project lists.
@MainActor
final class ConfigProjectService {
    static let configProjectName = "Wawa Note Config"
    static let configProjectSlug = "wawa-note-config"

    private init() {}

    /// Returns the config project, creating it if it doesn't exist.
    /// Returns nil only if creation fails — callers should gracefully degrade.
    static func ensureConfigProject(context: ModelContext) -> Project? {
        if let existing = findConfigProject(context: context) {
            return existing
        }
        return createConfigProject(context: context)
    }

    /// Whether a project is the config project.
    static func isConfigProject(_ project: Project) -> Bool {
        project.slug == configProjectSlug
    }

    /// Whether a project slug is the config project.
    static func isConfigProjectSlug(_ slug: String) -> Bool {
        slug == configProjectSlug
    }

    // MARK: - Private

    private static func findConfigProject(context: ModelContext) -> Project? {
        let predicate = #Predicate<Project> { $0.slug == configProjectSlug }
        let descriptor = FetchDescriptor<Project>(predicate: predicate)
        return try? context.fetch(descriptor).first
    }

    private static func createConfigProject(context: ModelContext) -> Project? {
        let svc = ProjectService(context: context)
        let project: Project
        do {
            project = try svc.create(name: configProjectName, origin: .system)
            // Set summary post-creation since create() no longer takes summary directly
            _ = try? svc.update(
                id: project.id,
                fields: ProjectUpdateFields(summary: "App configuration: providers, prompts, settings, and agent memories. Edit files directly to customize Wawa Note."),
                origin: .system
            )
        } catch {
            AppLog.general.error("ConfigProjectService: failed to create config project — \(error.localizedDescription)")
            return nil
        }
        // Override the auto-generated slug to ensure it's always wawa-note-config
        project.slug = configProjectSlug
        project.iconName = "gearshape.fill"
        project.colorHex = "#64748B" // slate gray — neutral, distinct from user projects
        project.intention = "System configuration project. Contains AI providers, prompt templates, app settings, and agent memories."
        project.intentionIsAutoGenerated = true
        project.isHidden = true
        try? context.save()
        AppLog.event("config", "Created wawa-note-config project")
        return project
    }

    // MARK: - Sync

    /// Sync the config project items with the current app state.
    /// Uses diff-based updates: only modifies items whose content has changed,
    /// preserving manual edits and item identity (id, createdAt, tags).
    static func syncConfigProject(context: ModelContext) {
        guard let project = findConfigProject(context: context) else { return }
        let psvc = ProjectService(context: context)

        let existing = (try? psvc.items(in: project.id)) ?? []
        let configPrefixes = ["Provider: ", "Prompt: ", "App Settings", "Agent Memories", "Memory: ", "Skill: ", "Lens: ", "Model Preset: "]
        var existingByTitle: [String: KnowledgeItem] = [:]
        for item in existing where configPrefixes.contains(where: { item.title.hasPrefix($0) || item.title == $0 }) {
            existingByTitle[item.title] = item
        }
        var seenTitles = Set<String>()
        var changedCount = 0
        var createdCount = 0
        var skippedCount = 0

        // Helper: upsert item — update if exists and changed, create if new, skip if identical
        func upsertItem(title: String, body: String, tags: [String]) {
            seenTitles.insert(title)
            if let existingItem = existingByTitle[title] {
                if existingItem.bodyText == body {
                    skippedCount += 1
                    return
                }
                existingItem.bodyText = body
                existingItem.tags = tags
                changedCount += 1
            } else {
                let item = KnowledgeItem(type: .note, title: title, status: .draft,
                    tags: tags, folderID: nil, bodyText: body,
                    durationSeconds: nil, languageCode: nil, inboxDate: nil)
                context.insert(item)
                item.projectID = project.id
                createdCount += 1
            }
        }

        // 1. Providers — expose all fields including key status
        let configs = (try? context.fetch(FetchDescriptor<AIProviderConfigModel>())) ?? []
        let keyStore = SecureKeyStore()
        for config in configs {
            let keyPresent = config.apiKeyKeychainIdentifier.map { (try? keyStore.loadAPIKey(for: $0)) != nil } ?? false
            let dict: [String: Any] = [
                "name": config.name,
                "type": config.typeRaw,
                "defaultModel": config.defaultModel,
                "baseURL": config.baseURLString as Any,
                "supportsStreaming": config.supportsStreaming,
                "supportsTools": config.supportsTools,
                "supportsAudio": config.supportsAudio,
                "supportsEmbeddings": config.supportsEmbeddings,
                "keyStatus": keyPresent ? "present" : "missing",
                "availableModels": config.availableModels,
                "notes": config.notes as Any
            ]
            let json = (try? JSONSerialization.data(withJSONObject: dict, options: .prettyPrinted))
                .flatMap { String(data: $0, encoding: .utf8) } ?? "{}"
            upsertItem(title: "Provider: \(config.name)", body: json, tags: ["config", "provider"])
        }

        // 2. Prompts — preserve user-edited flag, include usage info
        let prompts = PromptStore.shared.prompts(in: nil)
        for prompt in prompts {
            let meta: [String: Any] = [
                "name": prompt.name,
                "category": prompt.category,
                "isUserEdited": prompt.isUserEdited,
                "variables": prompt.variables
            ]
            let metaJSON = (try? JSONSerialization.data(withJSONObject: meta, options: .prettyPrinted))
                .flatMap { String(data: $0, encoding: .utf8) } ?? "{}"
            upsertItem(title: "Prompt: \(prompt.name)",
                body: "# Metadata\n\(metaJSON)\n\n# Content\n\(prompt.content)",
                tags: ["config", "prompt"])
        }

        // 3. Settings — tagged with readOnly vs editable sections
        let settingsDict = buildSettingsSnapshot()
        let settingsJSON = (try? JSONSerialization.data(withJSONObject: settingsDict, options: .prettyPrinted))
            .flatMap { String(data: $0, encoding: .utf8) } ?? "{}"
        upsertItem(title: "App Settings", body: settingsJSON, tags: ["config", "settings"])

        // 4. Memories — individual items per memory for granular editing
        let memories = AgentMemoryStore.shared.listAll()
        for mem in memories {
            let memDict: [String: Any] = [
                "id": mem.id.uuidString,
                "pattern": mem.pattern,
                "strategy": mem.strategy,
                "itemType": mem.itemType as Any,
                "contentType": mem.contentType as Any,
                "language": mem.language as Any,
                "minDuration": mem.minDuration as Any,
                "minChars": mem.minChars as Any,
                "successCount": mem.successCount,
                "failCount": mem.failCount,
                "isStale": mem.isStale,
                "relevance": mem.relevance,
                "lastUsed": ISO8601DateFormatter().string(from: mem.lastUsed),
                "createdAt": ISO8601DateFormatter().string(from: mem.createdAt)
            ]
            let memJSON = (try? JSONSerialization.data(withJSONObject: memDict, options: .prettyPrinted))
                .flatMap { String(data: $0, encoding: .utf8) } ?? "{}"
            upsertItem(title: "Memory: \(mem.pattern)", body: memJSON, tags: ["config", "memory"])
        }

        // 5. Skills (from AnalysisSkillStore)
        let skills = AnalysisSkillStore.shared.skills(in: nil)
        for skill in skills {
            let skillDict: [String: Any] = [
                "id": skill.id.uuidString,
                "name": skill.name,
                "displayName": skill.displayName,
                "description": skill.description,
                "category": skill.category,
                "templateID": skill.templateID,
                "defaultModel": skill.defaultModel,
                "maxIterations": skill.maxIterations,
                "allowedTools": skill.allowedTools,
                "isUserEdited": skill.isUserEdited
            ]
            let skillJSON = (try? JSONSerialization.data(withJSONObject: skillDict, options: .prettyPrinted))
                .flatMap { String(data: $0, encoding: .utf8) } ?? "{}"
            upsertItem(title: "Skill: \(skill.name)", body: skillJSON, tags: ["config", "skill"])
        }

        // 6. Lenses (from AIConfigService)
        let lenses = AIConfigService.shared.config.lenses ?? [:]
        for (lensId, lens) in lenses {
            let lensDict: [String: Any] = [
                "id": lensId,
                "name": lens.name,
                "description": lens.description,
                "systemPrompt": lens.systemPrompt,
                "userPrompt": lens.userPrompt as Any
            ]
            let lensJSON = (try? JSONSerialization.data(withJSONObject: lensDict, options: .prettyPrinted))
                .flatMap { String(data: $0, encoding: .utf8) } ?? "{}"
            upsertItem(title: "Lens: \(lens.name)", body: lensJSON, tags: ["config", "lens"])
        }

        // 7. Model Presets (from AIConfigService)
        let presets = AIConfigService.shared.config.modelPresets ?? [:]
        for (modelName, preset) in presets {
            let presetDict: [String: Any] = [
                "model": modelName,
                "supportsTemperature": preset.supportsTemperature as Any,
                "maxOutputTokens": preset.maxOutputTokens as Any,
                "supportsJSONFormat": preset.supportsJSONFormat as Any
            ]
            let presetJSON = (try? JSONSerialization.data(withJSONObject: presetDict, options: .prettyPrinted))
                .flatMap { String(data: $0, encoding: .utf8) } ?? "{}"
            upsertItem(title: "Model Preset: \(modelName)", body: presetJSON, tags: ["config", "model-preset"])
        }

        // 8. Remove orphaned items (titles no longer present in current state)
        for (title, item) in existingByTitle where !seenTitles.contains(title) {
            item.projectID = nil
            context.delete(item)
            changedCount += 1
        }

        // Single save
        try? context.save()
        AppLog.event("config", "Synced wawa-note-config: \(createdCount)c/\(changedCount)u/\(skippedCount)s — \(configs.count) providers, \(prompts.count) prompts, settings, \(memories.count) memories, \(skills.count) skills, \(lenses.count) lenses")
    }

    /// Build a complete settings snapshot from all UserDefaults and app state.
    static func buildSettingsSnapshot() -> [String: Any] {
        let defs = UserDefaults.standard
        var s: [String: Any] = [:]

        // App info
        s["appVersion"] = Bundle.main.infoDictionary?["CFBundleShortVersionString"] as? String ?? "unknown"
        s["buildNumber"] = Bundle.main.infoDictionary?["CFBundleVersion"] as? String ?? "unknown"

        // Transcription
        s["transcriptionMode"] = defs.string(forKey: UserDefaultsKey.transcriptionMode) ?? "apple"
        s["transcriptionAllowCloud"] = defs.bool(forKey: UserDefaultsKey.transcriptionAllowCloud)

        // Automation
        s["autoTranscribe"] = defs.object(forKey: UserDefaultsKey.autoTranscribe) as? Bool ?? true
        s["autoAnalyze"] = defs.object(forKey: UserDefaultsKey.autoAnalyze) as? Bool ?? true
        s["autoAnalysisModel"] = defs.string(forKey: UserDefaultsKey.autoAnalysisModel) ?? ""
        s["autoAnalysisProvider"] = defs.string(forKey: UserDefaultsKey.autoAnalysisProvider) ?? ""

        // Audio
        s["voiceProcessing"] = !defs.bool(forKey: UserDefaultsKey.audioRawMode)
        s["speakerphoneMode"] = defs.bool(forKey: UserDefaultsKey.audioSpeakerphoneMode)

        // Developer
        s["developerModeEnabled"] = defs.bool(forKey: UserDefaultsKey.developerModeEnabled)

        // Active provider
        s["activeProviderId"] = ActiveProviderManager.shared.getActiveProviderID() ?? "none"

        // Anarlog
        s["anarlogAutoImport"] = defs.object(forKey: UserDefaultsKey.anarlogAutoImport) as? Bool ?? true
        s["anarlogAutoExport"] = defs.object(forKey: UserDefaultsKey.anarlogAutoExport) as? Bool ?? false

        // AI Config summary
        let aiCfg = AIConfigService.shared.config
        s["aiProviders"] = Array(aiCfg.providers.keys)
        s["aiFeatures"] = aiCfg.features.map { Array($0.keys) } ?? []
        s["aiModelPresets"] = aiCfg.modelPresets?.count ?? 0
        s["aiLenses"] = aiCfg.lenses.map { Array($0.keys) } ?? []

        // Summary cache stats
        let cacheStats = SummaryCache.shared.stats
        s["summaryCacheEntries"] = cacheStats.entries
        s["summaryCacheHitRate"] = String(format: "%.1f%%", cacheStats.hitRate * 100)

        return s
    }

    // MARK: - Reverse Flow (Config Project → App State)

    /// Apply configuration from config project items back to the app's actual state.
    /// This is the reverse of syncConfigProject: reads KnowledgeItems and updates
    /// AIProviderConfigModel, PromptStore, UserDefaults, AgentMemoryStore, etc.
    /// Returns counts of applied changes.
    static func applyConfigFromProject(context: ModelContext) -> [String: Int] {
        guard let project = findConfigProject(context: context) else {
            AppLog.config.warning("applyConfigFromProject: config project not found")
            return [:]
        }
        let psvc = ProjectService(context: context)
        let items = (try? psvc.items(in: project.id)) ?? []
        var counts: [String: Int] = ["providers": 0, "prompts": 0, "settings": 0, "memories": 0, "errors": 0]

        for item in items {
            guard let bodyData = item.bodyText?.data(using: .utf8) else { continue }

            if item.title.hasPrefix("Provider: ") {
                guard let json = try? JSONSerialization.jsonObject(with: bodyData) as? [String: Any],
                      let name = json["name"] as? String else { counts["errors"]! += 1; continue }
                let configs = (try? context.fetch(FetchDescriptor<AIProviderConfigModel>())) ?? []
                if let existing = configs.first(where: { $0.name == name }) {
                    if let newModel = json["defaultModel"] as? String { existing.defaultModel = newModel }
                    if let newURL = json["baseURL"] as? String { existing.baseURLString = newURL }
                    if let notes = json["notes"] as? String { existing.notes = notes }
                    counts["providers"]! += 1
                }
            }

            if item.title.hasPrefix("Prompt: ") {
                // Parse the hybrid Markdown+JSON format
                let parts = item.bodyText?.components(separatedBy: "\n# Content\n") ?? []
                if parts.count >= 2,
                   let metaJSON = parts[0].replacingOccurrences(of: "# Metadata\n", with: "").data(using: .utf8),
                   let meta = try? JSONSerialization.jsonObject(with: metaJSON) as? [String: Any],
                   let name = meta["name"] as? String {
                    let content = parts[1...].joined(separator: "\n# Content\n") // rejoin if content contained '# Content'
                    let store = PromptStore.shared
                    if store.prompt(named: name) != nil {
                        store.updatePrompt(named: name, content: content)
                    } else {
                        _ = store.createPrompt(name: name, category: meta["category"] as? String ?? "custom", content: content)
                    }
                    counts["prompts"]! += 1
                }
            }

            if item.title == "App Settings" {
                guard let json = try? JSONSerialization.jsonObject(with: bodyData) as? [String: Any] else { continue }
                let defs = UserDefaults.standard
                if let v = json["autoTranscribe"] as? Bool { defs.set(v, forKey: UserDefaultsKey.autoTranscribe) }
                if let v = json["autoAnalyze"] as? Bool { defs.set(v, forKey: UserDefaultsKey.autoAnalyze) }
                if let v = json["autoAnalysisModel"] as? String { defs.set(v, forKey: UserDefaultsKey.autoAnalysisModel) }
                if let v = json["autoAnalysisProvider"] as? String { defs.set(v, forKey: UserDefaultsKey.autoAnalysisProvider) }
                if let v = json["voiceProcessing"] as? Bool { defs.set(!v, forKey: UserDefaultsKey.audioRawMode) }
                if let v = json["speakerphoneMode"] as? Bool { defs.set(v, forKey: UserDefaultsKey.audioSpeakerphoneMode) }
                if let v = json["developerModeEnabled"] as? Bool { defs.set(v, forKey: UserDefaultsKey.developerModeEnabled) }
                counts["settings"]! += 1
            }

            if item.title.hasPrefix("Memory: ") {
                guard let json = try? JSONSerialization.jsonObject(with: bodyData) as? [String: Any],
                      let pattern = json["pattern"] as? String,
                      let strategy = json["strategy"] as? String else { counts["errors"]! += 1; continue }
                AgentMemoryStore.shared.write(pattern: pattern, strategy: strategy,
                    itemType: json["itemType"] as? String,
                    contentType: json["contentType"] as? String,
                    language: json["language"] as? String,
                    minDuration: json["minDuration"] as? Double,
                    minChars: json["minChars"] as? Int)
                counts["memories"]! += 1
            }
        }

        try? context.save()
        AppLog.event("config", "Applied from config project: \(counts["providers"]!)p/\(counts["prompts"]!)pr/\(counts["settings"]!)s/\(counts["memories"]!)m (\(counts["errors"]!) errors)")
        return counts
    }

    // MARK: - Config Export/Import

    /// Export full configuration as a portable JSON bundle (no API keys).
    /// Returns JSON-serializable dictionary suitable for .wawaconfig export.
    static func exportConfigBundle(context: ModelContext) -> [String: Any] {
        var bundle: [String: Any] = [:]
        bundle["version"] = 1
        bundle["exportedAt"] = ISO8601DateFormatter().string(from: Date())

        // Providers (without API keys)
        let configs = (try? context.fetch(FetchDescriptor<AIProviderConfigModel>())) ?? []
        bundle["providers"] = configs.map { config in
            return [
                "name": config.name,
                "type": config.typeRaw,
                "defaultModel": config.defaultModel,
                "baseURL": config.baseURLString as Any,
                "supportsStreaming": config.supportsStreaming,
                "supportsTools": config.supportsTools,
                "supportsAudio": config.supportsAudio,
                "supportsEmbeddings": config.supportsEmbeddings,
                "availableModels": config.availableModels,
                "notes": config.notes as Any
            ] as [String: Any]
        }

        // Prompts (user overrides only)
        let userPrompts = PromptStore.shared.prompts(in: nil).filter { $0.isUserEdited }
        bundle["prompts"] = userPrompts.map { prompt in
            return [
                "name": prompt.name,
                "category": prompt.category,
                "content": prompt.content,
                "variables": prompt.variables
            ] as [String: Any]
        }

        // Settings
        bundle["settings"] = buildSettingsSnapshot()

        // Skills (user overrides only)
        let userSkills = AnalysisSkillStore.shared.skills(in: nil).filter { $0.isUserEdited }
        bundle["skills"] = userSkills.map { skill in
            return [
                "name": skill.name,
                "displayName": skill.displayName,
                "description": skill.description,
                "category": skill.category,
                "templateID": skill.templateID,
                "defaultModel": skill.defaultModel,
                "maxIterations": skill.maxIterations,
                "allowedTools": skill.allowedTools
            ] as [String: Any]
        }

        // Memories
        let memories = AgentMemoryStore.shared.listAll()
        if !memories.isEmpty,
           let data = try? JSONEncoder().encode(memories),
           let jsonArray = try? JSONSerialization.jsonObject(with: data) {
            bundle["memories"] = jsonArray
        }

        // Active provider ID (for re-import)
        bundle["activeProviderID"] = ActiveProviderManager.shared.getActiveProviderID() ?? ""

        return bundle
    }

    /// Import configuration from a previously exported JSON bundle.
    /// Providers are created without API keys (user must re-add keys in Settings).
    /// Returns counts of imported items.
    static func importConfigBundle(_ bundle: [String: Any], context: ModelContext) -> [String: Int] {
        var counts: [String: Int] = ["providers": 0, "prompts": 0, "skills": 0, "memories": 0]

        // Import providers
        if let providers = bundle["providers"] as? [[String: Any]] {
            for p in providers {
                guard let name = p["name"] as? String, !name.isEmpty,
                      let typeRaw = p["type"] as? String else { continue }
                let existing = (try? context.fetch(FetchDescriptor<AIProviderConfigModel>())).flatMap { $0 } ?? []
                if existing.contains(where: { $0.name == name && $0.typeRaw == typeRaw }) { continue }
                let config = AIProviderConfigModel(name: name, type: ProviderType(rawValue: typeRaw) ?? .openAICompatible)
                config.defaultModel = p["defaultModel"] as? String ?? ""
                config.baseURLString = p["baseURL"] as? String
                config.supportsStreaming = p["supportsStreaming"] as? Bool ?? true
                config.supportsTools = p["supportsTools"] as? Bool ?? true
                config.supportsAudio = p["supportsAudio"] as? Bool ?? false
                config.supportsEmbeddings = p["supportsEmbeddings"] as? Bool ?? false
                if let notes = p["notes"] as? String { config.notes = notes }
                context.insert(config)
                counts["providers"]! += 1
            }
        }

        // Import prompts
        if let prompts = bundle["prompts"] as? [[String: Any]] {
            for p in prompts {
                guard let name = p["name"] as? String,
                      let content = p["content"] as? String else { continue }
                let store = PromptStore.shared
                if store.prompt(named: name) != nil {
                    store.updatePrompt(named: name, content: content)
                } else {
                    _ = store.createPrompt(name: name, category: p["category"] as? String ?? "custom", content: content)
                }
                counts["prompts"]! += 1
            }
        }

        // Import skills
        if let skills = bundle["skills"] as? [[String: Any]] {
            for s in skills {
                guard let name = s["name"] as? String,
                      let displayName = s["displayName"] as? String else { continue }
                let skillStore = AnalysisSkillStore.shared
                if let existing = skillStore.skill(named: name) {
                    skillStore.updateSkill(named: name,
                        systemPrompt: s["systemPrompt"] as? String,
                        templateID: s["templateID"] as? String)
                } else {
                    let desc: String = s["description"] as? String ?? ""
                    let cat: String = s["category"] as? String ?? "imported"
                    let tid: String = s["templateID"] as? String ?? ""
                    let sp: String = s["systemPrompt"] as? String ?? ""
                    let dm: String = s["defaultModel"] as? String ?? ""
                    let mi: Int = s["maxIterations"] as? Int ?? 3
                    let at: [String] = s["allowedTools"] as? [String] ?? []
                    let newSkill = AnalysisSkill(
                        id: UUID(), name: name, displayName: displayName,
                        description: desc, category: cat,
                        templateID: tid, systemPrompt: sp,
                        defaultModel: dm, maxIterations: mi,
                        allowedTools: at, isUserEdited: true,
                        updatedAt: Date()
                    )
                    skillStore.createSkill(newSkill)
                }
                counts["skills"]! += 1
            }
        }

        // Import memories
        if let memories = bundle["memories"] as? [[String: Any]],
           let data = try? JSONSerialization.data(withJSONObject: memories),
           let decoded = try? JSONDecoder().decode([AgentMemory].self, from: data) {
            for mem in decoded {
                AgentMemoryStore.shared.write(pattern: mem.pattern, strategy: mem.strategy,
                    itemType: mem.itemType, contentType: mem.contentType,
                    language: mem.language)
            }
            counts["memories"]! = decoded.count
        }

        // Restore active provider
        if let activeID = bundle["activeProviderID"] as? String, !activeID.isEmpty {
            ActiveProviderManager.shared.setActiveProviderID(activeID)
        }

        try? context.save()
        AppLog.event("config", "Imported config: \(counts["providers"]!)p/\(counts["prompts"]!)pr/\(counts["skills"]!)s/\(counts["memories"]!)m")
        return counts
    }
}
